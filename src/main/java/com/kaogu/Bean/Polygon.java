package com.kaogu.Bean;

public class Polygon {

    private Integer polygon_id;
    private String polygon_name;
    private String polygon_path;

    public Integer getPolygon_id() {
        return polygon_id;
    }

    public void setPolygon_id(Integer polygon_id) {
        this.polygon_id = polygon_id;
    }

    public String getPolygon_name() {
        return polygon_name;
    }

    public void setPolygon_name(String polygon_name) {
        this.polygon_name = polygon_name;
    }

    public String getPolygon_path() {
        return polygon_path;
    }

    public void setPolygon_path(String polygon_path) {
        this.polygon_path = polygon_path;
    }
}
